        <script>
            let currentIndex = 0;

            function showSlide(index, container) {
                const totalSlides = container.querySelectorAll('.carousel-item').length;
                index = (index + totalSlides) % totalSlides; // Handle looping

                const translateValue = -index * 100 + '%';
                container.querySelector('.carousel-content').style.transform = 'translateX(' + translateValue + ')';
                currentIndex = index;
            }

            function prevSlide(button) {
                const container = button.closest('.Menu-container');
                showSlide(currentIndex - 1, container);
            }

            function nextSlide(button) {
                const container = button.closest('.Menu-container');
                showSlide(currentIndex + 1, container);
            }
        </script>
